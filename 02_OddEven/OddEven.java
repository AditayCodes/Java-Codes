
// This program checks if a number is even or odd

public class OddEven {
    public static void main(String[] args) {
        int number = 10; // Example number
        if (number % 2 == 0) {
            System.out.println(number + " is even.");
        } else {
            System.out.println(number + " is odd.");
        }
    }
}

// Output: 10 is even.
